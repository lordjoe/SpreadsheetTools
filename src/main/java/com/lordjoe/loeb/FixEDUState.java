    public static String getDomain(String email) {
        if (!email.endsWith(".edu"))
            return email;
        String[] items = email.split("\\.");
        int length = items.length;
        if (length > 2) {
            String domain = items[length - 2] + ".edu";
            return domain;
        }
        return email;
    }

    public static void rememberEDUState(String email, State state) {
        if(state == State.UNKNOWN)
            return;
        State existing = eduByState.get(email);
        if(existing == state)
            return;
        if(existing != null  )
            eduByState.put(email, state); // break herte

         eduByState.put(email, state);
        String domain = getDomain(email);
        if (!domain.equalsIgnoreCase(email))
            eduByState.put(domain, state);
    }

