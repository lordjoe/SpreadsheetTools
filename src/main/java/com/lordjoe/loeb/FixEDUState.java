    public static State getEmailState(String email) {
        buildEdyByState();

        State ret = eduByState.get(email);
        if(ret != null)
            return ret;
        String domain = getDomain(email);
        if (!domain.equalsIgnoreCase(email))
            ret = eduByState.get(email);
        if(ret != null)
            return ret;
        return State.UNKNOWN;
    }
